								<div class="section-heading">
									<h2 id="various-gas-laws">The Different Gas Laws</h2>
								</div>
								<div class="big-section-heading">
									<h2>Avogadro's Law</h2>
								</div>
								<div class="equations">
									<span>$$\frac{V_1}{n_1} = \frac{V_2}{n_2}$$</span> 
									<span>OR</span>
									<span>V<sub>1</sub> / n<sub>1</sub> = V<sub>2</sub> / n<sub>2</sub></span>
								</div>

								<p>Amadeo Avogadro believed that if the same pressure and temperature could be maintained, all gases would contain the same number of molecules at equal volumes. He was pretty accurate in his belief.</p>

								<ul>
									<li>At constant temperature and pressure, the volume taken by a gas sample is proportional to the number of moles <em>n</em> of gas</li>
									<li>This equation shows the relationship between the volume of a gas and its number of molecules</li>
									<li><strong>Standard molar volume</strong> is the volume taken up by a mole of gas at standard temperature and pressure</li>
									<li>The standard molar volume of an ideal gas is 22.414 liters per mole</li>
								</ul>
								<div class="big-section-heading">
									<h2>Boyle's Law</h2>
								</div>
								<div class="equations">
									<span>$${P_1V_1} = {P_2V_2}$$</span> 
									<span>OR</span>
									<span>P<sub>1</sub>V<sub>1</sub> = P<sub>2</sub>V<sub>2</sub></span>
								</div>

								<p>Boyle's Law is used for gas samples that are at a constant temperature. It predicts the behavior of gases very well at common temperatures.</p>

								<ul>
									<li>If you're working on a problem where a sample's pressure and volume change, there is a good chance you need to use Boyle's Law</li>
									<li>If you are working on a problem where you know the <em>volume</em> at one pressure and need to figure out the volume a different pressure, and if the temperature stays the same, you probably need to use Boyle's Law</li>
									<li>If you are working on a problem where you know the <em>pressure</em> at a certain volume and need to figure out the pressure at another volume, and if the temperature does not change, then you probably need to use Boyle's Law as well</li>
								</ul>

								<div class="big-section-heading">
									<h2>Charles' Law</h2>
								</div>
								<div class="equations">
									<span>$$\frac{V_1}{T_1} = \frac{V_2}{T_2}$$</span> 
									<span>OR</span>
									<span>V<sub>1</sub> / T<sub>1</sub> = V<sub>2</sub> / T<sub>2</sub></span>
								</div>

								<p>With Charles' Law, volume and increases as the temperature increases when the amount of pressure stays the same</p>

								<ul>
									<li>This law is often considered to be the basis for the absolute temperature scale</li>
									<li>When using Charles' law, you generally need to use Kelvins for the temperature unit</li>
									<li>Many practice problems that are based on this law require you to convert from Fahrenheit or Celsius to Kelvin first</li>
								</ul>
							</div>
