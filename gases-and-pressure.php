								<div class="section-heading">
									<h2 id="common-problem-combined-gas-law">Common Problem: Combined Gas Law | Find Volume at STP</h2>
								</div>

								
								<div class="common-problem">
									<p>It is fairly common to be given a problem where you need to figure out the volume of a gas sample at standard temperature and pressure. You will be given <em>V<sub>1</sub></em>, <em>T<sub>1</sub></em>, <em>P<sub>1</sub></em>, and asked to find <em>V<sub>2</sub></em></p>
									<p class="question">A gas sample occupies 87 L at 12.5 &#176;C under a pressure of 822 torr. What volume would it occupy at STP?</p>
								</div>

								<div class="big-section-heading">
									<h2 id="">Explanation</h2>
								</div>

								<p>This type of problem can be tricky. You need to figure out <em>V<sub>2</sub></em>, but you are seemingly not given <em>P<sub>2</sub></em> or <em>T<sub>2</sub></em>. However, the key to solving questions like this is that you were told that the sample is at standard temperature and pressure, which means that you can assume the second set of values needed to calculate <em>V<sub>2</sub></em>.<br><strong> At STP, <em>T<sub>2</sub></em> is 273 <em>K</em> and <em>P<sub>2</sub></em> is 760 torr</strong>.</p>

								<p>With these units known, the equation can be set up...<br></p>


								<div class="subheading">
									<h2>Original Equation</h2>
								</div>

								<div class="equations">
									<span>$$\frac{P_1T_1}{V_1} = \frac{P_2T_2}{V_2}$$</span>

									<span>OR</span>


									<span><em>P<sub>1</sub>T<sub>1</sub></em> / <em>V<sub>1</sub></em> = <em>P<sub>2</sub>T<sub>2</sub></em> / <em>V<sub>2</sub></em></span>

								</div>

								<div class="subheading">
									<h2>Revised Equation</h2>
								</div>

								<div class="equations">
									<span>$$V2 = \frac{P_1V_1T_2}{T_1P_2}$$</span>
									<span>OR</span>
									<span>V2 = <em>[(P<sub>1</sub>)(V<sub>1</sub>)(T<sub>2</sub>)]</em> / <em>[(T<sub>1</sub>)(P<sub>2</sub>)]</em></span>

