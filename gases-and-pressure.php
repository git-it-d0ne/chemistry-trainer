									<h2 id="combined-gas-law">Combined Gas Law Equation</h2>
								</div>

								<p>Boyle's Law and Charles' Law can be joined into what is called the combined gas law equation</p>
								<div class="equations">
									<span>$$\frac{P_1V_1}{T_1} = \frac{P_2V_2}{T_2}$$</span> 
									<span>OR</span>
									<span>P<sub>1</sub>V<sub>1</sub> / T<sub>1</sub> = P<sub>2</sub>V<sub>2</sub> / T<sub>2</sub></span>
								</div>
								<ul>
									<li>When five of the six variables are known, you can figure out the sixth</li>
								</ul>
