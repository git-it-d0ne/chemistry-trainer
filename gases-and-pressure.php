								<div class="section-heading">
									<h2 id="ideal-gas-law">Ideal Gas Law</h2>
								</div>

								<p>An ideal gas is one that strictly obeys the gas laws, but most gases do not actually adhere to the gas laws perfectly. However, at temperatures and pressures that are not extreme, the amount that the gases vary from the laws are generally tolerable</p>
								<ul>
									<li>1 mole of an ideal gas takes up 22.414 L at 1.0000 atm and 273.15 K</li>
								</ul>

								<p>The ideal gas law is a combination of the previously discussed laws that is bound together by something called a proportionality constant, which is represented with <em>R</em></p>
								<ul>
									<li>This gives rise to the equation <em>PV</em> = <em>nRT</em></li>
									<li>This is often referred to as the ideal gas equation or ideal gas law</li>
									<li><em>R</em> = <em>PV</em> / <em>nT</em></li>
									<li>When solving for R using the values listed above for 1 mole of an ideal gas, <em>R</em> = 0.082057 <sup>(L)(atm)</sup> / <sub>(mol)(<em>K</em>)</sub></li>
									<li>You will often see <em>R</em> rounded to 0.0821 <sup>(L)(atm)</sup> / <sub>(mol)(<em>K</em>)</sub></li>
								</ul>
