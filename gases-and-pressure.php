									<h2 id="kinetic-molecular-theory">Kinetic Molecular Theory of Gases</h2>
								</div>

								<p>The Kinetic Molecular Theory provides a fantastic explanation for the behavior of gases.</p>

								<ul>
									<li>Gases consist of small particles that move at high velocities in seemingly random straight lines</li>
									<li>Gases constantly move in those straight lines until they collide with something, altering their path</li>
									<li>The average kinetic energy of the gas particles is propertional to the temperature in Kelvin</li>
									<li>Gas particles occupy very little volume compared to the amount of volume the overal gas can occupy</li>
									<li>There are <em>very small</em> attractive forces between gas particles</li>
								</ul>
