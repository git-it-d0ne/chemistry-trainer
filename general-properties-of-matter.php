								<div class="section-heading">
									<h2 id="matter-and-its-phases">What is Matter?</h2>
								</div>

								<p>Matter is the material that makes up the universe. Matter is made up from element atoms, which are the smallest part of an element that can keep their identity.</p>


								<div class="big-section-heading">
									<h2 id="matter-and-its-phases-atoms">Atoms</h2>
								</div>

								<p>Atoms are the smallest particles of elements that maintain their identity during a physical or chemical change. They are made of electrons, protons, and neutrons. <span class="emphasize">They have a very dense, positively charged nucleus that is surrounded by electrons</span>.</p>
								<ul>
									<li>Electrons have a negative charge and are often symbolized with <em>e</em><sup>-</sup></li>
									<li>Neutrons have no charge and are often represented as <em>n</em> but you might also see <em>n</em><sup>0</sup></li>
									<li>Protons have a positive charge and are often symbolized with <em>p</em> or <em>p</em><sup>+</sup></li>
									<li>Protons and neutrons are similar sizes and they are about 1800 times larger than electrons.</li>
									<li>Atomic number, or <em>Z</em>, shows the number of protons in an atom's nucleus, and each element's atomic number is unique to it</li>
									<li>If an atom is neutral. it contains the same amount of electrons outside the nucleus as protons in the nucleus</li>
									<li>An atom's mass number is the sum of its protons and neutrons</li>
									<li>The number of protons and electrons will be the same in an uncharged atom</li>
								</ul>

								<div class="big-section-heading">
									<h2 id="matter-and-its-phases-nucleus">The Nucleus</h2>
								</div>

								<p>The nucleus of an atom contains the atom's protons and neutrons.</p>

								<ul>
									<li>The majority of an atom's mass is in its nucleus</li>
									<li></li>
									<li></li>
									<li></li>
								</ul>

								<div class="big-section-heading">
									<h2 id="matter-and-its-phases-protons">Protons</h2>
								</div>

								<p></p>

								<ul>
									<li>An atom's number of protons never changes</li>
									<li></li>
									<li></li>
								</ul>

								<div class="big-section-heading">
									<h2 id="matter-and-its-phases-neutrons">Neutrons</h2>
								</div>

								<p></p>

								<ul>
									<li>Atoms can have various amounts of neutons</li>
									<li>This means that the mass number of an atom can change</li>
									<li>Atomic numbers for atoms never change since an atom cannot gain or lose protons</li>
								</ul>

								<div class="big-section-heading">
									<h2 id="matter-and-its-phases-electrons">Electrons</h2>
								</div>

								<p>Electrons are negatively charged particles that reside in a space around the nucleus called the electron cloud.</p>

								<ul>
									<li>The majority of an atom's volume resides within the electron cloud</li>
									<li>Electrons form chemical bonds</li>
									<li>Erwin Schrodinger believed that an atom's electrons take up various shells in the electron cloud that surrounds the nucleus</li>
									<li>The first shell is closest to the nucleus</li>
									<li>The second, third, and fourth shells are all further out than the last</li>									
								</ul>

								<div class="big-section-heading">
									<h2 id="matter-and-its-phases-isotopes">Isotopes</h2>
								</div>

								<p>Isotopes are versions of an element with different mass numbers.</p>

								<ul>
									<li>Isotopes are represented with a superscript that precedes an element's chemical symbol</li>
									<li>For example, <sup>3</sup>He is an isotope of helium</li>
									<li>An element's atomic mass is the weighted average of its known isotopes</li>
									<li>The molecular mass of an element is the sum of the atomic masses of each atom in that element</li>
								</ul>

