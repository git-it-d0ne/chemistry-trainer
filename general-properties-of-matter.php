									<h2 id="">Sublimation</h2>
								</div>
								<p>Sublimation is when a solid changes directly to a gas, skipping the liquid phase.</p>
								<ul>
									<li>Sublimation is caused by the rapid spreading out of particles</li>
									<li>After sublimation occurs, the gas will immediately turn into a solid once it starts to cool, skipping the liquid phase again</li>
								</ul>
