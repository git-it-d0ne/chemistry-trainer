								<div class="section-heading">
									<h2 id="matter-and-its-phases-phases-of-matter">Phases of Matter</h2>
								</div>

								<p>It can exist in 3 different forms (referred to as phases or states), which are solid, liquid, and gas.  The particles that make up matter are always moving, and increased temperature makes them move faster while a decrease in temperature makes them move slower. The weight of these particles also influences their speed.</p>


								<p><span class="emphasize">Solids</span> are a phase of matter with definite volume and shape.</p>
								<ul>
									<li>They are composed of tightly-bound particles that are held together by very strong attractive forces.</li> 
									<li>These particles vibrate around one another.</li> 
									<li>When heated, solids slightly increase in size through a process known as expansion.</li> 
									<li>When cooled, solids slightly decrease in size through a process known as contraction.</li>
								</ul>

								
								<p><span class="emphasize">Liquids</span> are a phase of matter with indefinite shape and definite volume.</p> 
								<ul>
									<li>The particles in liquids are very close together, but they can roll around each other, which allows liquids to take the shape of their containers.</li> 
									<li>The forces of attraction in liquids are weaker than they are in solids, but liquid particles usually have more energy than solid particles.</li> 
									<li>Temperature has a slight influence on the volume of liquids.</li>
									<li>Volume can be slightly reduced with pressure through the process of compression.</li>
								</ul>

								
								<p><span class="emphasize">Gases</span> are a phase of matter with indefinite shape and indefinate volume.</p>
								<ul>
									<li>Gases will take the shape of their container AND spread out evenly through it.</li> 
									<li>The volume of gases is heavily influenced by temperature.</li> 
									<li>Particles in gases are far apart and move around randomly at a high velocity.</li>
									<li>There are very weak forces of attraction in gases.</li> 
									<li>Volume can be substantially reduced with pressure through the process of compression.</li>
								</ul>


								<p><span class="emphasize">Diffusion is </span>the process through which gases spread out.</p>
								<ul>
									<li>Different gases and liquids diffuse at different rates</li>
									<li>Diffusion can also occur in liquids, but it is much slower</li>
									<li><span class="emphasize">Intimate mixing</span> is the diffusion between liquids and gases</li>
								</ul>



								<div class="big-section-heading">
									<h2 id="changes-in-phase-melting-and-boiling-points">Melting and Boiling Points</h2>
								</div>

								<p>A substance's <span class="emphasize">phase changes based on the melting and boiling point of that substance</span>. Basically, the state that a substance is in depends on its temperature.  For example, if a solid substance has a melting point of 100 &#176;C, it will turn into a liquid if heated to that temperature. If the substance has a boiling point of 120 &#176;C, it will start turning into a gas at that temperature.</p> <p>When heat is added, phases change from solid -> liquid -> gas. When the substance is cooled, its phases will change back in the order of gas -> liquid -> solid.</p>

								<ul>
									<li>Melting and boiling points can be used to figure out the identity of unknown substances.</li>
									<li>All you have to do is record the temperatures needed to melt and boil the substances and reference it against the periodic table of elements</li>
									<li>When a solid has reached its melting point its particles gain the ability to move around each other instead of just vibrating together</li>
									<li>A solid will not reach a higher temperature than its melting point until it has been melted</li>
									<li>When a liquid has reached its boiling point gas bubbles will start to form inside the liquid and rise up while the liquid evaporates</li>
									<li>Gases condense into liquids and liquids freeze into solids</li>
								</ul>

