									<h2 id="">Physical Changes</h2>
								</div>

								<p>A physical change is a type of change where the chemical composition of the matter remains unaltered. It does not change.</p>
								<ul>
									<li>Changes in phase are physical changes</li>
									<li>Physical changes are changes where new substances are <em>not</em> formed</li>
								</ul>

