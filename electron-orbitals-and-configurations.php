								<div class="section-heading">
									<h2 id="what-are-electron-orbitals">What are Electron Orbitals?</h2>
								</div>
								<p>Electron orbitals, also called wave functions, represent a prediction of where an electron is at in an atom relative to its nucleus at that point in time. This is the result of Heisenberg's uncertainty principle, which is an argument that an electron's exact position cannot be known, but its general location can be predicted fairly accurately.</p>

								<ul>
									<li>Electron orbitals have certain directional properties, shapes, and sizes</li>
									<li>The location of an electron relative to its nucleus is predicted by squaring the wave function &Psi;<sup>2</sup></li>
									<li>The principal quantum number <em>n</em>, represents the energy of an orbital along with something called its shell</li>
									<li>The four atomic orbitals are: s, p, d, and f</li>
								</ul>

								<div class="big-section-heading">
									<h2 id="what-are-electron-orbitals-shells">Electron Orbital Shells</h2>
								</div>

								<p>Electron shells</p>

								<ul>
									<li>Each electron shell has 1 s orbital</li>
									<li>Each electron shell after the first one has 3 p orbitals</li>
									<li>Each electron shell after the second one has 5 d orbitals</li>
									<li>Each electron shell after the third one has 7 f orbitals</li>
									<li>Up to two electrons can exist in each of the orbitals</li>

								</ul>

								<div class="big-section-heading">
									<h2 id="what-are-electron-orbitals-orbital-placement">Rules for Figuring Out which Orbital an Electron Takes</h2>
								</div>

								<p>There are three rules that determine the orbital that an electron occupies. These rules are known as the Aufbau Principle, the Paulie Exclusion Principle, and Hund's Rule</p>

								<ul>
									<li>The Aufbau Principle states that an electron will always go into the orbital with the lowest energy<br> (s < p < d < f)</li>
									<li>The Paulie Exclusion Principle argues that no more than two electrons can occupy an atomic orbital and those two electrons must have an opposite spin</li>
									<li>Hund's rule argues that when two or more atomic oribtals have the same energy, an electron will occupy the empty orbital before it pairs with another electron</li>
										<ul>
											<li>This means that each p, d, or f orbital will each have one electron in them before any electrons start to pair</li>
										</ul>
								</ul>

								<div class="big-section-heading">
									<h2 id="what-are-electron-orbitals-valence-and-core-electrons">Valence and Core Electrons</h2>
								</div>

								<p>There are two types of electrons that you will likely need to know about in your course: valence and core electrons</p>

								<ul>
									<li>Valence electrons are the electrons that are in an atom's outermost shell</li>
									<li>Core electrons are the electrons that are in an atom's inner shell</li>
									<li>Valence electrons play a substantial role in chemical bonding and determining an element's chemical behavior</li>
									<li>Core electrons are just kind of there, hoping to one day become valence electrons</li>
								</ul>

								<div class="big-section-heading">
