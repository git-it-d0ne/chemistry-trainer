								<div class="big-section-heading">
									<h2>Wavelength</h2>
								</div>
								<div class="equations">
									<span>$$\lambda = \hbar / mv$$</span> 
								</div>

								<p>A scientist named Louis de Broglie correctly argued that particles like electrons probably have wavelike characteristics</p>

								<ul>
									<li>The basic idea is that particles with a mass and velocity should have a wavelength</li>
									<li>Wavelength is represented with the symbol &Lambda;, which is called lambda</li>
									<li>A wavelength is basically the distance between identical, adjacent wave points</li>
									<li>The value of a wavelength can be calculated by multiplying the particle's mass and volume, and then dividing Planck's constant by that value</li>
									<li>Your instructor or textbook might tell you to that a wavelength is the quotient of Planck's constant and the product of the particle's mass and volume, don't worry, it means the same thing as the last bullet point</li>
									<li>Planck's constant is 6.626 * 10<sup>-34</sup></li>
								</ul>

								<div class="big-section-heading">
									<h2>Frequency</h2>
								</div>
								<p>Frequency is the number of wave crests that pass a certain point in a specified amount of time.</p>

								<ul>
									<li>Frequency is represented by the symbol &nu;, which is called nu</li>
									<li>Frequency is often measured in cycles per second</li>
